public class EscapeSequence{
    public static void main(String[] args){

        //Escape Sequence
        System.out.println("HI");
        System.out.print("HELLO");
        // double quotation
        System.out.println("Yo \"Hello\" fr");
        //backslash
        System.out.println("This is backslash: \\");
        System.out.println("This file is located at ~\\Desktop\\FunnyCatPics");
        //tab
        System.out.println("This is a tab \tYOOOO!!!");
        //line changes
        System.out.println("This\nis\non\nmultiple\nlines");

    }
}