public class LiteralPractise{
    public static void main(String[] args){
        
        long bigNum = 2000000L;
        System.out.println(bigNum);
        
        float randFloat = 22.45f;
        System.out.println(randFloat);
        
        char symbol = '\u2764';
        System.out.println(symbol);
        
    }
}